

package modelos;


public class Operaciones {

    
    private int a;
    private int b;
    
    public Operaciones(int a, int b){
        this.a = a;
        this.b = b;
    }
    
    public int sumar(int a , int b){
        
        
        int sumar = a+b;
        
        return sumar;
        

    }
    
    
    public int restar(int a , int b){
        
        
        int restar = a-b;
        
        return restar;
        
        
        
    }

}
